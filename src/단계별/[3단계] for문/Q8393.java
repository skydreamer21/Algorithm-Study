import java.util.Scanner;

public class Q8393 {
   public static void main(String args[]) {
       Scanner sc = new Scanner(System.in);
       int T;
       int x;
       int y;
       T=sc.nextInt();
       for (int i=0;i<T;i++) {
           x=sc.nextInt();
           y=sc.nextInt();
           System.out.println(x+y);
       }
   }
}







